def _parse_button(name):
    try:
        return getattr(mouse.Button, name)
    except Exception:
        return mouse.Button.left


def _parse_key(value):
    if not value:
        return None
    if isinstance(value, str) and value.startswith('Key.'):
        name = value.split('.', 1)[1]
        return getattr(keyboard.Key, name, value)
    return value


def _play_events(event_list, context_name):
    m = mouse.Controller()
    k = keyboard.Controller()
    controller = ExecutionController()
    controller.set_macro_context(context_name, len(event_list))
    last = 0.0
    for idx, (t, kind, data) in enumerate(event_list, start=1):
        controller.update_event_position(idx - 1, idx, len(event_list))
        controller.poll_keypress()
        controller.wait_if_paused()
        if controller.stop_requested:
            controller.close()
            raise StopRequested(controller.get_stop_message())
        if isinstance(data, dict) and '_event_index' not in data:
            data['_event_index'] = idx
        wait = t - last
        if wait > 0:
            if not sleep_with_controls(wait, controller):
                controller.close()
                raise StopRequested(controller.get_stop_message())
        last = t
        controller.poll_keypress()
        controller.wait_if_paused()
        if controller.stop_requested:
            controller.close()
            raise StopRequested(controller.get_stop_message())
        if handle_custom_event(kind, data, controller):
            continue
        if kind == 'move':
            controller.set_locked_mouse_position(data['x'], data['y'])
            m.position = (data['x'], data['y'])
        elif kind == 'click':
            controller.set_locked_mouse_position(data['x'], data['y'])
            m.position = (data['x'], data['y'])
            btn = _parse_button(data['button'])
            if data['pressed']:
                m.press(btn)
            else:
                m.release(btn)
        elif kind == 'scroll':
            controller.set_locked_mouse_position(data['x'], data['y'])
            m.position = (data['x'], data['y'])
            m.scroll(data['dx'], data['dy'])
        elif kind == 'key_down':
            key = _parse_key(data.get('key'))
            if key is not None:
                k.press(key)
        elif kind == 'key_up':
            key = _parse_key(data.get('key'))
            if key is not None:
                k.release(key)
    controller.close()


def play_pre():
    _play_events(events_pre, __name__ + '.antes_identificador')


def play_post():
    _play_events(events_post, __name__ + '.depois_identificador')


