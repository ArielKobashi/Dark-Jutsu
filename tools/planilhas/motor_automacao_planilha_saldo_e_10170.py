    print(f"Motor iniciado. Linhas válidas encontradas: {total}")
    print("Fonte dos dados: coluna A = código | coluna I = valor digitado")
    print("A automação começa em 5 segundos. Clique no sistema/campo inicial agora.")
    print("Para parar: pressione Ctrl+C no PowerShell.")
    time.sleep(5)

    try:
        for pos, dados_linha in enumerate(linhas, start=1):
            if MOSTRAR_LOG:
                print(
                    f"[{pos}/{total}] Linha Excel {dados_linha['linha_excel']} | "
                    f"Código: {dados_linha['codigo']} | "
                    f"Valor coluna I: {dados_linha['valor_teclado']}"
                )

            executar_macro_para_linha(dados_linha)

            if pos < total and PAUSA_ENTRE_LINHAS > 0:
                time.sleep(PAUSA_ENTRE_LINHAS)

        print("Motor finalizado.")

    except KeyboardInterrupt:
        print("\nAutomação interrompida pelo usuário.")


