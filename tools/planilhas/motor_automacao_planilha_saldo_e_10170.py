def _parse_button(name):
    try:
        return getattr(mouse.Button, name)
    except Exception:
        return mouse.Button.left

def _parse_key(value):
    if not value:
        return None
    if isinstance(value, str) and value.startswith('Key.'):
        name = value.split('.', 1)[1]
        return getattr(keyboard.Key, name, value)

    # Teclas do teclado numérico gravadas como <96>..<105>.
    # 96=0, 97=1, ..., 105=9.
    if isinstance(value, str) and value.startswith('<') and value.endswith('>'):
        try:
            vk = int(value[1:-1])
            if 96 <= vk <= 105:
                return str(vk - 96)
        except ValueError:
            pass

    return value


def _valor_para_texto(valor):
    """Converte valor do Excel em texto seguro para colar/digitar."""
    if valor is None:
        return ""
    if isinstance(valor, float) and valor.is_integer():
        return str(int(valor))
    return str(valor).strip()


def _resolver_planilha():
    caminho = Path(PLANILHA_DADOS)
    if not caminho.is_absolute():
        caminho = Path(__file__).resolve().parent / caminho
    if not caminho.exists():
        raise FileNotFoundError(
            f"Planilha não encontrada: {caminho}\n"
            f"Coloque o arquivo '{PLANILHA_DADOS}' na mesma pasta deste .py "
            "ou ajuste PLANILHA_DADOS no topo do código."
        )
    return caminho


def carregar_linhas_da_planilha():
    caminho = _resolver_planilha()
    wb = load_workbook(caminho, data_only=True)
    ws = wb[ABA_DADOS] if ABA_DADOS else wb.active

    linhas = []
    for linha in range(LINHA_INICIAL, ws.max_row + 1):
        codigo = _valor_para_texto(ws[f"{COLUNA_CODIGO}{linha}"].value)
        valor_teclado = _valor_para_texto(ws[f"{COLUNA_VALOR_TECLADO}{linha}"].value)

        # Ignora linha completamente vazia.
        if not codigo and not valor_teclado:
            continue

        # Sem código não tem como executar a busca/inserção principal.
        if not codigo:
            if MOSTRAR_LOG:
                print(f"Linha {linha}: ignorada porque a coluna {COLUNA_CODIGO} está vazia.")
            continue

        linhas.append({
            "linha_excel": linha,
            "codigo": codigo,
            "valor_teclado": valor_teclado,
        })

    wb.close()
    return linhas


def _pressionar_ctrl_v(k):
    k.press(keyboard.Key.ctrl_l)
    time.sleep(0.05)
    k.press('v')
    time.sleep(0.05)
    k.release('v')
    time.sleep(0.05)
    k.release(keyboard.Key.ctrl_l)


def _digitar_texto(k, texto):
    texto = _valor_para_texto(texto)
    if texto:
        k.type(texto)


def executar_macro_para_linha(dados_linha):
    m = mouse.Controller()
    k = keyboard.Controller()

    codigo = dados_linha["codigo"]
    valor_teclado = dados_linha["valor_teclado"]

    last = 0.0
    skip_until = 0

    for idx, (t, kind, data) in enumerate(events, start=1):
        # Consome eventos substituídos sem executar a ação gravada.
        if idx <= skip_until:
            last = t
            continue

        wait = t - last
        if wait > 0:
            time.sleep(wait)
        last = t

        # Substitui o Ctrl+V gravado pelo código da coluna A.
        if idx == EVENTO_COLAR_CODIGO_INICIO:
            pyperclip.copy(codigo)
            _pressionar_ctrl_v(k)
            skip_until = EVENTO_COLAR_CODIGO_FIM
            continue

        # Substitui apenas o bloco escolhido pelo valor da coluna I.
        # O bloco fixo 10170 permanece gravado e continua executando.
        if idx == EVENTO_DIGITAR_COLUNA_I_INICIO:
            _digitar_texto(k, valor_teclado)
            skip_until = EVENTO_DIGITAR_COLUNA_I_FIM
            continue

        if kind == 'move':
            m.position = (data['x'], data['y'])
        elif kind == 'click':
            m.position = (data['x'], data['y'])
            btn = _parse_button(data['button'])
            if data['pressed']:
                m.press(btn)
            else:
                m.release(btn)
        elif kind == 'scroll':
            m.position = (data['x'], data['y'])
            m.scroll(data['dx'], data['dy'])
        elif kind == 'key_down':
            key = _parse_key(data.get('key'))
            if key is not None:
                k.press(key)
        elif kind == 'key_up':
            key = _parse_key(data.get('key'))
            if key is not None:
                k.release(key)


